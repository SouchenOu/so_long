#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*p;
	int		cmp;

	cmp = 0;
	p = lst;
	while (p != NULL)
	{
		cmp++;
		p = p->next;
	}
	return (cmp);
}
