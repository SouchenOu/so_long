#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*p;

	p = lst;
	if (!lst)
	{
		return ;
	}
	while (p != NULL)
	{
		f (p->content);
		p = p->next;
	}
}
